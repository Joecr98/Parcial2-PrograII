    private ClsUsuario usuarioMod = new ClsUsuario();
    private ClsUsuarioJDBC tbUsuarios = new ClsUsuarioJDBC();
    
    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        String empleado = this.txUsuario.getText();
        String enero = this.txPass.getText();
        try {
            this.usuarioMod.setUsername(empleado);
            this.usuarioMod.setPassword(Integer.parseInt(enero)+"");

        }catch(Exception e){
            System.out.println("Datos no validos: "+e);
        }
        if(usuarioMod.datosMinimosLlenos()){
            this.tbUsuarios.insert(usuarioMod);  
            this.padre.agregarNuevo();
        }        
        /* ------- LIMPIAR -------*/
        this.txUsuario.setText("");
        this.txPass.setText("");
    }//GEN-LAST:event_btnRegistrarMouseClicked

