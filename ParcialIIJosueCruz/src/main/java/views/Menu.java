    public void agregarNuevo() {        
        ClsEmpleadoJDBC tbEmpleado = new ClsEmpleadoJDBC();        
        ClsEmpleado nuevo = tbEmpleado.obtenerUltimo();
        
        Object[] fila = new Object[7];
        fila[0] = nuevo.getIdemple();
        fila[1] = nuevo.getEmpleado();
        fila[2] = nuevo.getEnero();
        fila[3] = nuevo.getFebrero();
        fila[4] = nuevo.getMarzo();
        fila[5] = nuevo.getTotal();
        fila[6] = nuevo.getPromedio();             
        this.baseTabla.addRow(fila);
        this.tbEmpleados.setModel(baseTabla);
    }
    
    private void setDatos() {
        ClsEmpleadoJDBC alJDBC = new ClsEmpleadoJDBC();
        List<ClsEmpleado> dataTabla = alJDBC.seleccion();
        Object[] fila = new Object[7];
        
        for (ClsEmpleado emple : dataTabla){
            fila[0] = emple.getIdemple();
            fila[1] = emple.getEmpleado();
            fila[2] = emple.getEnero();
            fila[3] = emple.getFebrero();
            fila[4] = emple.getMarzo();
            fila[5] = emple.getTotal();
            fila[6] = emple.getPromedio(); 
            
            System.out.println("Empleado="+emple);
            this.baseTabla.addRow(fila);
        }
        this.tbEmpleados.setModel(baseTabla);
    }
    
