    private ClsEmpleado empleadoMod;
    private int filaEdit;
    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        this.empleadoMod = new ClsEmpleado();
        int fila = this.tbEmpleados.getSelectedRow();
        filaEdit = fila;
        if(fila >= 0){
            String idEmple = this.tbEmpleados.getValueAt(fila, 0).toString();
            String empleado = this.tbEmpleados.getValueAt(fila, 1).toString();
            String enero =  this.tbEmpleados.getValueAt(fila, 2).toString();
            String febrero = this.tbEmpleados.getValueAt(fila, 3).toString();
            String marzo = this.tbEmpleados.getValueAt(fila, 4).toString();
            try {
                this.empleadoMod.setIdemple(Integer.parseInt(idEmple));
                this.empleadoMod.setEmpleado(empleado);
                this.empleadoMod.setEnero(Integer.parseInt(enero));
                this.empleadoMod.setFebrero(Integer.parseInt(febrero));
                this.empleadoMod.setMarzo(Integer.parseInt(marzo)); 
            }catch(Exception e){
                System.out.println("Datos no validos: "+e);
            }
            if(empleadoMod.datosMinimosLlenos()){ 
                FormularioActualizar fActualizar = new FormularioActualizar(this, true, empleadoMod);
                fActualizar.setVisible(true);
            }   
        }else{
            System.out.println(fila);
        }
    }//GEN-LAST:event_btnActualizarMouseClicked
        
    public void actualizarFila(ClsEmpleado empleActualizado) {
        Object[] fila = new Object[7];
        fila[0] = empleActualizado.getIdemple();
        fila[1] = empleActualizado.getEmpleado();
        fila[2] = empleActualizado.getEnero();
        fila[3] = empleActualizado.getFebrero();
        fila[4] = empleActualizado.getMarzo();
        fila[5] = empleActualizado.getTotal();
        fila[6] = empleActualizado.getPromedio();             
        this.baseTabla.setValueAt(fila[0], filaEdit, 0);     
        this.baseTabla.setValueAt(fila[1], filaEdit, 1);     
        this.baseTabla.setValueAt(fila[2], filaEdit, 2);     
        this.baseTabla.setValueAt(fila[3], filaEdit, 3);     
        this.baseTabla.setValueAt(fila[4], filaEdit, 4);     
        this.baseTabla.setValueAt(fila[5], filaEdit, 5);     
        this.baseTabla.setValueAt(fila[6], filaEdit, 6);
        this.tbEmpleados.setModel(baseTabla);
    }
    
